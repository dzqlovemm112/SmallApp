#import "HTHoldNullObj.h"

@implementation HTHoldNullObj
+ (NSString *)getValueWithUnCheakValue:(NSObject *)value{
    
        if (![value isNull] ) {
            return [NSString stringWithFormat:@"%@", value];
        }else{
            return @"";
        }

}

@end
